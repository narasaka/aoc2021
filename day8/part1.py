inpf = "in"
with open(inpf, "r") as f:
    segments = f.read().strip().split('\n')

lens = {2, 3, 4, 7}

ans = 0
for segment in segments:
    output = segment.strip().split('|')[1].split()
    for digit in output:
        if len(digit) in lens:
            ans += 1

print(ans)


